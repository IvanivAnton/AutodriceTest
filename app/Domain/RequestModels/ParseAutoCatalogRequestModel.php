<?php

namespace App\Domain\RequestModels;

class ParseAutoCatalogRequestModel
{
    private string $path;

    /**
     * @param string $path
     */
    public function __construct(string $path)
    {
        $this->path = $path;
    }

    /**
     * @return string
     */
    public function getPath(): string
    {
        return $this->path;
    }


}
